void setup()
{
  SPI.begin();

  // Config SPI speed
  SPI.setClockDivider(SPI_CLOCK_DIV16);
  // Config pins
  pinMode(dst_pin, OUTPUT);
  pinMode(latch_pin, OUTPUT);

  digitalWrite(SS, HIGH);
  digitalWrite(dst_pin, LOW);
  digitalWrite(latch_pin, HIGH);

  stepper.setMaxSpeed(1000);
}

